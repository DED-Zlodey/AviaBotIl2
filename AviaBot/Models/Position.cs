namespace AviaBot.Models;

public class Position
{
	/// <summary>
	/// Координата X, представляющая положение объекта в пространстве
	/// </summary>
	public double X { get; set; }

	/// <summary>
	/// Координата Y позиции объекта
	/// </summary>
	public double Y { get; set; }

	/// <summary>
	/// Координата Z позиции объекта
	/// </summary>
	public double Z { get; set; }
}
